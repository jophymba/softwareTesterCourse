class Kalkulacka():
    
    prvni = None
    druhe = None

    def scitani(self):
        
        return self.prvni + self.druhe
        

    def odcitani(self):

        return self.prvni - self.druhe


    def nasobeni(self):

        return self.prvni * self.druhe


    def deleni(self):

        return self.prvni / self.druhe