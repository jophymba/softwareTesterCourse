smajlik = input("Zadej smajlíka: \n")
if (smajlik == ":-)" ):
    print("Tvůj smajlík je veselý")

elif (smajlik == ":-(" ):
    print("Tvůj smajlík je smutný")

elif (smajlik == ":-*" ):
    print("Tvůj smajlík je zamilovaný") 

elif (smajlik == ":-P" ):
    print("Tvůj smajlík je s vyplazeným jazykem")

else:
    print("Tvůj smajlík je neznáný")    